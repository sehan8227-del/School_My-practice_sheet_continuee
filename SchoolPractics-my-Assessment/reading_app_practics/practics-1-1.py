# 2. - **목표:** 새로운 도서/콘텐츠 기록 추가

#- **연습 내용:**
#---------------------------
# 단일 콘텐츠 기록 예시
content = {
    "title": "데스노트",
    "category": "콘텐츠",
    "format": "만화",
    "memo": "스토리 구조 관찰"
}

# 리스트에 추가
contents.append(content)
#--------------------------
#- **손 연습 포인트:**
#    - 딕셔너리 만들기
#    - append로 리스트에 넣기
#    - print(contents)로 확인
#-----------------------------
# 3.- **목표:** 저장된 콘텐츠 리스트 확인

#- **연습 내용:**
#----------------------------
# 모든 콘텐츠 출력
for idx, c in enumerate(contents, start=1):
    print(f"{idx}. {c['title']} ({c['category']})")
#--------------------------------
#- **손 연습 포인트:**
#    - for문과 enumerate 사용
#    - 딕셔너리 값 접근 (`c['title']`)
#    - 리스트와 딕셔너리의 구조 이해

