#좋아! 그럼 지금 바로 **Read(조회) 연습용 미니 루트**를 만들어 줄게.

#핵심은 **리스트 안 딕셔너리를 꺼내고, 출력하며 데이터 구조 감각을 익히는 것**이야. append나 삭제는 아직 신경 쓰지 않고, 순수하게 **읽고 출력하는 과정**만 연습하도록 구성했어.


# 1. 예제 콘텐츠 미리 정의
contents = [
    {"title": "슬램덩크 1권", "category": "만화"},
    {"title": "데스노트", "category": "만화"},
    {"title": "해리포터 1권", "category": "소설"}
]

# 2. 모든 콘텐츠 목록 출력 연습
print("=== 모든 콘텐츠 목록 ===")
for idx, c in enumerate(contents, start=1):
    print(f"{idx}. {c['title']} ({c['category']})")

# 3. 특정 항목 출력 연습
print("\n=== 첫 번째 콘텐츠 상세 ===")
first = contents[0]  # 리스트에서 첫 번째 항목 꺼내기
print("제목:", first["title"])
print("카테고리:", first["category"])

# 4. 순번과 값 접근 연습
print("\n=== 순번과 제목 확인 ===")
for idx, c in enumerate(contents, start=1):
    print(f"순번 {idx} - 제목: {c['title']}")

#**연습 포인트**:

#1. `enumerate(contents, start=1)` → 리스트 순서와 데이터를 같이 꺼내고 싶을 때 사용
#2. `c['title']` → 딕셔너리 안 특정 키 값 접근
#3. `contents[0]` → 리스트에서 특정 항목 직접 꺼내기
#4. 출력 순서를 눈으로 확인하며 **데이터 구조 흐름 감각 익히기**

#이걸 **손으로 타이핑 → 실행 → 출력 확인**만 해도 충분히 Read 구조 감각을 익힐 수 있어.

